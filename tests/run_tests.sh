#!/bin/bash
cd /Users/user/.gemini/antigravity/scratch/MacTV_KVM

# Сброс цвета терминала
NC='\033[0m'
GREEN='\033[0;32m'
RED='\033[0;31m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'

echo "=========================================================="
echo " ⚙️ ЗАПУСК ЕДИНОГО ТЕСТ-РАННЕРА ДЛЯ KVM-МОСТА"
echo "=========================================================="

# Освобождаем порт 12345 и убиваем фоновые процессы KVM для изоляции тестов
echo "   🧹 Очистка портов и завершение фоновых KVM процессов..."
lsof -ti :12345 | xargs kill -9 2>/dev/null
killall tv_kvm 2>/dev/null
killall -f "tv_remote_bridge.js" 2>/dev/null
sleep 1

ALL_PASSED=true

# -----------------------------------------------------------------------------
# ЭТАП 1: Компиляция и запуск автоматических Swift UI/Unit тестов
# -----------------------------------------------------------------------------
echo -e "\n${BLUE}👉 ЭТАП 1: Запуск тестов Cocoa-клиента (Swift)...${NC}"
echo "   🛠️ Компиляция Swift в TDD режиме (-DTESTING)..."

swiftc tv_kvm.swift -DTESTING -Xlinker -sectcreate -Xlinker __TEXT -Xlinker __info_plist -Xlinker Info.plist -framework Speech -framework AVFoundation -o tv_kvm_tests 2>/dev/null

if [ $? -ne 0 ]; then
  echo -e "   ${RED}❌ Ошибка компиляции Swift-тестов.${NC}"
  ALL_PASSED=false
else
  echo "   🚀 Запуск скомпилированных тестов интерфейса..."
  ./tv_kvm_tests
  
  if [ $? -ne 0 ]; then
    echo -e "   ${RED}❌ Swift тесты провалены.${NC}"
    ALL_PASSED=false
  else
    echo -e "   ${GREEN}✅ Swift-тесты интерфейса пройдены успешно!${NC}"
  fi
  
  # Очищаем бинарный файл тестов
  rm -f tv_kvm_tests
fi

# -----------------------------------------------------------------------------
# ЭТАП 2: Запуск автоматических Node.js интеграционных тестов (User Stories)
# -----------------------------------------------------------------------------
echo -e "\n${BLUE}👉 ЭТАП 2: Запуск юзерстори-тестов бэкенда (Node.js)...${NC}"

if [ ! -d "node_modules" ]; then
  echo "   📦 Установка npm зависимостей..."
  npm install --cache ./npm-cache >/dev/null 2>&1
fi

node tests/usability_stories.js

if [ $? -ne 0 ]; then
  echo -e "   ${RED}❌ Интеграционные тесты бэкенда провалены.${NC}"
  ALL_PASSED=false
else
  echo -e "   ${GREEN}✅ Интеграционные тесты бэкенда пройдены успешно!${NC}"
fi

# -----------------------------------------------------------------------------
# Итоги тестирования
# -----------------------------------------------------------------------------
echo -e "\n=========================================================="
if [ "$ALL_PASSED" = true ] ; then
  echo -e " 🎉 ${GREEN}РЕЗУЛЬТАТ: Все тесты успешно пройдены! (100% OK)${NC}"
  echo "=========================================================="
  exit 0
else
  echo -e " 🛑 ${RED}РЕЗУЛЬТАТ: Обнаружены проваленные тесты.${NC}"
  echo "=========================================================="
  exit 1
fi
