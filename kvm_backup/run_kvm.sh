#!/bin/bash
cd /Users/user/.gemini/antigravity/scratch
echo "===================================================="
echo " 🛠️ Компиляция нативного KVM-моста на Swift..."
echo "===================================================="
swiftc tv_kvm.swift -o tv_kvm
if [ $? -eq 0 ]; then
  echo " 🎉 Сборка успешна! Запускаем KVM-мост..."
  echo " Нажмите Ctrl+C для полного выключения программы."
  echo "===================================================="
  ./tv_kvm
else
  echo " ❌ Ошибка компиляции. Похоже, у вас не установлены Xcode Command Line Tools."
  echo " Вы можете установить их одной быстрой командой: xcode-select --install"
  echo "===================================================="
fi
